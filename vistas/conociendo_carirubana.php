            imgData.append('id', $("#textiddocumento").val());

            fetch('../php/guardar_conociendo_carirubana_img.php', {
                method: 'POST',
                body: imgData
            }).then(res => res.json()).then(data => {
                not.push(data);
                var res = data;
                if (res == 1) {
                    $.notify({
                        icon: 'la flaticon-success',
                        title: 'Agregado',
                        message: 'Sus imagen han sido registrada exitosamente!',
                      },{
                        type: 'info',
                        placement: {
                          from: "bottom",
                          align: "right"
                        },
                        time: 1000,
                    });
                    $("#tb-conociendo-carirubana").DataTable().ajax.reload();
                    $("#info_documento_table").css('display', 'none');
                    $("#info_documento").css('display', 'none');
                    $("#tabla_vista").css('display', 'block');
                    myDropzone.removeAllFiles(true);
                } else if (res == 0) {
                    $.notify({
                        icon: 'la flaticon-success',
                        title: 'Error',
                        message: 'Ha ocurrido un error inesperado!',
                      },{
                        type: 'danger',
                        placement: {
                          from: "bottom",
                          align: "right"
                        },
                        time: 1000,
                    });
                } else if (res == 2) {
                    alert("Ha ocurrido un error, intente de nuevo");
                }
            });
        }

        if (!not.includes('fail')) {
